		data = append(data, []byte{0x89, 0xF0, 0x3D, 0x01, 0x00, 0x00, 0x00, 0x0F, 0x8D}...)
		data = append(data, []byte{0xBF, 0x02, 0x00, 0x00, 0x00, 0xB8, 0x3C, 0x00, 0x00, 0x40, 0x0F, 0x05}...)
