func runtimeSmokeTestELF(target string) []byte {
	data := make([]byte, 20)
	copy(data[:4], "\x7fELF")
	data[4] = 1
	if target == "linux-x86" {
		data[18] = 0x03
	} else {
		data[18] = 0x3e
	}
	return data
}

