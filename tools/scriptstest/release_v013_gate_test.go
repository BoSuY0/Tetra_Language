func TestReleaseV013GateRejectsNonDirectoryReportPathBeforeSideEffects(t *testing.T) {
	root := releaseOldGateMinimalRoot(t, "scripts/release/v0_1_3/gate.sh")
	reportDir := filepath.Join(root, "report-file")
	if err := os.WriteFile(reportDir, []byte("not a directory\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	assertOldGateRejectsNonDirectoryReportPath(t, root, "scripts/release/v0_1_3/gate.sh", "release_v0_1_3_gate:", reportDir)
}

func TestReleaseV013GateRejectsDanglingReportDirSymlinkBeforeSideEffects(t *testing.T) {
	root := releaseOldGateMinimalRoot(t, "scripts/release/v0_1_3/gate.sh")
	reportDir := filepath.Join(root, "dangling-report-link")
	if err := os.Symlink(filepath.Join(root, "missing-report-target"), reportDir); err != nil {
		t.Fatal(err)
	}

	assertOldGateRejectsNonDirectoryReportPath(t, root, "scripts/release/v0_1_3/gate.sh", "release_v0_1_3_gate:", reportDir)
}

func TestReleaseV013GateRejectsNonEmptyReportDirBeforeSideEffects(t *testing.T) {
	root := releaseOldGateMinimalRoot(t, "scripts/release/v0_1_3/gate.sh")
	reportDir := filepath.Join(root, "report")
	if err := os.MkdirAll(reportDir, 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(reportDir, "summary.json"), []byte("{}\n"), 0o644); err != nil {
		t.Fatal(err)
	}

	assertOldGateRejectsNonEmptyReportDir(t, root, "scripts/release/v0_1_3/gate.sh", "release_v0_1_3_gate:", reportDir)
}

