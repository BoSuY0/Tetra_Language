func TestReleaseV013GateIsCanonicalPatchGate(t *testing.T) {
	root := repoRoot(t)
	assertLegacyFileRemoved(t, "scripts/release_v0_1_3_gate.sh", "scripts/release/v0_1_3/gate.sh directly")
	raw, err := os.ReadFile(filepath.Join(root, "scripts", "release", "v0_1_3", "gate.sh"))
	if err != nil {
		t.Fatalf("read v0.1.3 release gate: %v", err)
	}
	text := string(raw)
	for _, want := range []string{
		"Usage: bash scripts/release/v0_1_3/gate.sh [--report-dir DIR]",
		`release_version="${TETRA_RELEASE_GATE_VERSION:-v0.1.3}"`,
		`release_artifact="${TETRA_RELEASE_GATE_ARTIFACT:-tetra.release.v0_1_3.gate-report.v1}"`,
		`release_gate_command="${TETRA_RELEASE_GATE_COMMAND:-bash scripts/release/v0_1_3/gate.sh}"`,
		`echo "== $release_version preflight =="`,
		`# Tetra $release_version Release Gate Report`,
		`run_step "version preflight ($release_version required)" check_release_version`,
		`expected $release_version version`,
		`--expected-version "$release_version"`,
		"Generated by",
		"scripts/release/v0_1_3/gate.sh",
	} {
		if !strings.Contains(text, want) {
			t.Fatalf("v0.1.3 release gate missing %q", want)
		}
	}
}

